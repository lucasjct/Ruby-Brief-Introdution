# Abstração
# abstrair entidades do mundo real e representá-las em classe.

# Encapsulamento
# dividir o programa em pequenas partes para torná-lo flexível, fácil de ler e codificar.

# Herança
# Criar classes com métodos e atributos(propriedades) que podem ser herdadas por outras.

# Polimorfismo
# chamar o mesmo método que irá reagir de forma diferente em diferentes objetos.

# Classes e OBjetos

# Classes
# É uma descrição de como será o objeto, seu comportamento, atributos(propriedades).

# Objetos
# Objetos é a representação do objeto do mundo real a partir do que foi definido na classe.

# Em POO, informações são atributos e comportamento chamamos de método.
